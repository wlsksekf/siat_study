"""
_02_03_02_module 모듈

모듈이란 함수·클래스·변수를 담아 재사용할 수 있도록 만든 파이썬 파일(.py)입니다. 

이 모듈은 기본적인 사칙연산 기능을 제공합니다.
- add: 덧셈
- subtract: 뺄셈
- multiply: 곱셈
- divide: 나눗셈
"""

def add(a, b):
    """두 수를 더합니다."""
    return a + b


def subtract(a, b):
    """두 수를 뺍니다."""
    return a - b

def multiply(a, b):
    """두 수를 곱합니다."""
    return a * b

def divide(a, b):
    """두 수를 나눠요."""
    return a / b


