a, b = map(int, input("첫 번째와 두 번째 숫자를 입력하세요: ").split())

print(f"{a} + {b} =", a + b)
print(f"{a} - {b} =", a - b)
print(f"{a} * {b} =", a * b)

print(f"{a} / {b} =", a / b)

print(f"{a} // {b} =", a // b)

print(f"{a} % {b} =", a % b)

print(f"{a} ** {b} =", a ** b)

print(f"{a} / {b} = {a / b:.2f}")